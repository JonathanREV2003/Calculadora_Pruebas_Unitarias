package org.example;

public class Metodos_calcu {
    public int suma(int a, int b){
        int suma;
        suma = a + b;
        System.out.println("resultado: "+suma);
        return suma;
    }
    public void resta(int a1, int b2){
        int resta;
        resta = a1 - b2;
        System.out.println("resultado: "+resta);
    }

    public void multiplicacion(int j, int k){
        int multiplicacion;
        multiplicacion = j * k;
        System.out.println("resultado: "+multiplicacion);

    }
    public void division(int x, int y){
        int division;
        division = x / y;
        System.out.println("resultado: "+division);
    }
}
